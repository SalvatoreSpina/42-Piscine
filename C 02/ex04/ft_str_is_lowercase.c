int	ft_str_is_lowercase(char *str)
{
	int	n;
	int	nope;

	nope = 1;
	n = 0;
	while (str[n])
	{
		if (str[n] >= 97 && str[n] <= 122)
			nope = 1;
		else
			return (0);
		n++;
	}
	return (nope);
}
