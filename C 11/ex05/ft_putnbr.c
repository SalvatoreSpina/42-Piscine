#include "ft_do_op.h"
#include <unistd.h>

void	ft_print(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		write (1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		ft_print('-');
		nb = nb * -1;
	}
	{
		if (nb > 9)
			ft_putnbr(nb / 10);
		ft_print(nb % 10 + '0');
	}
}
