int	ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int	i;
	int	n1;
	int	n2;

	n1 = f(tab[i - 1], tab[i]);
	i = 1;
	while (i < length && !(n1))
		i++;
	while (i < length)
	{
		n2 = f(tab[i - 1], tab[i]);
		if ((n1 < 0 && n2 > 0) || (n1 > 0 && n2 < 0))
			return (0);
		i++;
	}
	return (1);
}
