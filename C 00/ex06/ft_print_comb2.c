#include <unistd.h>

void	ft_print(char c)
{
	write(1, &c, 1);
}

void	ft_print_comb2(void)
{
	char	a1;
	char	a2;

	a1 = 0;
	a2 = 0;
	while (a1 <= 99)
	{
		a2 = a1 + 1;
		while (a2 <= 99)
		{
			ft_print(a1 / 10 + '0');
			ft_print(a1 % 10 + '0');
			ft_print(' ');
			ft_print(a2 / 10 + '0');
			ft_print(a2 % 10 + '0');
			a2++;
			if (a1 != 98)
				write(1, ", ", 2);
		}
		a1++;
	}
}
